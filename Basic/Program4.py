#4.	Write a Python program that will accept the base and height of a triangle and compute the area.

#------------------------Program-------------------------#
heightOfTriangle = int(input("Enter the Hieght of Triangle: "))
baseOfTriangle = int(input("Enter the Base of Triangle: "))
areaOfTriangle = 0.5*heightOfTriangle*baseOfTriangle
print("Area of Triangle: ",areaOfTriangle)